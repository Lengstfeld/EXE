/**
 *\brief Realiza la suma de 2 numeros.
 *\param A y B que seran reemplazado por los que le pasaremos.
 *\return El resultado.
 */
int logicaSuma(int vA, int vB);
/**
 *\brief Realiza la resta de 2 numeros.
 *\param A y B que seran reemplazado por los que le pasaremos.
 *\return El resultado.
 */
int logicaResta(int vA, int vB);
/**
 *\brief Realiza la division de 2 numeros.
 *\param A y B que seran reemplazado por los que le pasaremos.
 *\return El resultado mientras no se hallen 0 entre ninguno de los valores.
 */
float logicaDivision(int vA, int vB);
/**
 *\brief Realiza la multiplicacion de 2 numeros.
 *\param A y B que seran reemplazado por los que le pasaremos.
 *\return El resultado.
 */
int logicaMultiplicacion(int vA, int vB);
/**
 *\brief Realiza el factorial del 1er valor ingresado.
 *\param A y B que seran reemplazado por los que le pasaremos.
 *\return El resultado.
 */
int logicaFactorialA(int vA, int vB);
/**
 *\brief Realiza el factorial del 2do valor ingresado.
 *\param A y B que seran reemplazado por los que le pasaremos.
 *\return El resultado.
 */
int logicaFactorialB(int vA, int vB);
